    private void tableEtudiantMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableEtudiantMouseClicked
        // TODO add your handling code here:
      int selectedRow = tableEtudiant.getSelectedRow();
    if (selectedRow != -1) {
        id = Integer.parseInt(model.getValueAt(selectedRow, 0).toString());
        txtNom.setText(model.getValueAt(selectedRow, 1).toString());
        txtPrenom.setText(model.getValueAt(selectedRow, 2).toString());
        txtEmail.setText(model.getValueAt(selectedRow, 3).toString());
    }
      
    }//GEN-LAST:event_tableEtudiantMouseClicked

    private void bnModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnModifierActionPerformed
        // TODO add your handling code here
        int response = JOptionPane.showConfirmDialog(this, "Voulez-vous vraiment modifier cet étudiant ?");
    if (response == JOptionPane.YES_OPTION) {
        String nom = txtNom.getText();
        String prenom = txtPrenom.getText();
        String email = txtEmail.getText();
        if (es.update(new Etudiant(id, nom, prenom, email))) {
            JOptionPane.showMessageDialog(this, "Étudiant bien modifié");
            load();
        } else {
            JOptionPane.showMessageDialog(this, "Problème lors de la modification");
        }
    }
    }//GEN-LAST:event_bnModifierActionPerformed


