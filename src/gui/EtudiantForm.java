    private void bnSupprimeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnSupprimeActionPerformed
       int response = JOptionPane.showConfirmDialog(this, "Voulez-vous vraiment supprimer cet étudiant ?");
    if (response == JOptionPane.YES_OPTION) {
        Etudiant etudiantToDelete = es.findById(id);
        if (etudiantToDelete != null) {
            es.delete(etudiantToDelete);
            load();
        } else {
            JOptionPane.showMessageDialog(this, "Étudiant non trouvé.");
        }
    }
    }//GEN-LAST:event_bnSupprimeActionPerformed

